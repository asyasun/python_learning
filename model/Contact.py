class Contact:

    def __init__(self, name, sec_name, last_name, nick, company, mobile, email, birth_day, birth_month, birth_year):
        self.name = name
        self.sec_name = sec_name
        self.last_name = last_name
        self.nick = nick
        self.company = company
        self.mobile = mobile
        self.email = email
        self.birth_day = birth_day
        self.birth_month = birth_month
        self.birth_year = birth_year
